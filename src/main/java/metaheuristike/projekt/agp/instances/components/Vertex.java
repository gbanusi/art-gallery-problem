package metaheuristike.projekt.agp.instances.components;

/**
 * Class representing vertex in 2D.
 * @author jelena
 *
 */
public class Vertex {
	
	/** x coordinate */
	public double x;
	/** y coordinate */
	public double y;

	/**
	 * Class constructor.
	 * @param x x value.
	 * @param y y value.
	 */
	public Vertex(double x, double y) {
		this.x = x;
		this.y = y;
	}
}
